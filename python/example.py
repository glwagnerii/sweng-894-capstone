"""
Example Module

This contains functions to add and subtract numbers.

Functions:
    add_nums(a, b): Adds the input parameters together.
    subtract_nums(a, b): Subtracts the input parameters.
"""


def add_nums(a, b):
    """Adds the two input variables together.

    param a: First input variable, defaults to None
    :type a: int
    :param b: Second input variable, defaults to None
    :type b: int

    raises [ErrorType]: [ErrorDescription]

    :return: Sum of input variables.
    :rtype: int
    """

    return a + b


def subtract_nums(a, b):
    """Subtracts the two input variables together.

    param a: First input variable, defaults to None
    :type a: int
    :param b: Second input variable, defaults to None
    :type b: int

    raises [ErrorType]: [ErrorDescription]

    :return: Difference of input variables.
    :rtype: int
    """

    return a - b
